import base64

from openai import OpenAI

from config.loader import app_config
import os

client = OpenAI(api_key=app_config.gpt.api_key)


# Function to encode the image
def encode_image(image_path):
    with open(image_path, "rb") as image_file:
        return base64.b64encode(image_file.read()).decode("utf-8")


def get_response(image_path: str):
    image = encode_image(image_path)
    image_name = os.path.basename(image_path)
    response = client.responses.create(
        model=app_config.gpt.model,
        input=[  # type: ignore
            {
                "role": "user",
                "content": [
                    {"type": "input_text", "text": f"""
Ты помощник по документам. Внимательно изучи PDF документ и извлеки следующие данные:

НАЗВАНИЕ ФАЙЛА: {image_name}

"GTD": номер ГТД в формате: 12345 / 01.01.2025 / 1234567
- первое значение в номере ГТД может иметь только 1 из 2 значений: либо 27014 либо 12003

"vehicleId": номер автомобиля или вагона. Ищи:
- В названии файла: если файл называется "60166GBA.pdf", то номер автомобиля "60166GBA"
- После слов "АВТО", "ВАГОН", "АВТОМОБИЛЬ", "ЖД: " в документе (например, "ЖД: 12345678" → номер 12345678)
- В графе 21 "Транспортное средство на границе"
- Любые буквенно-цифровые коды, похожие на номера транспорта
- Если есть слэш "/", бери только часть до слэша
- если есть двоеточие ":", бери только часть после двоеточия
- Не возвращай короткие коды вроде "А8", "В8", если длина <4 или цифр <2.

"weightB": Вес брутто в кг. ВНИМАТЕЛЬНО ищи:
- В графе 35 "Вес брутто (кг)" или просто "35"
- Числа в поле с подписью "брутто"
- Если есть два веса (например, нетто 19491 кг и брутто), то брутто обычно больше нетто
- Ищи в таблицах любые числа рядом с "кг" которые больше веса нетто
- Может быть написано как "Вес брутто", "брутто", "Gross weight"
- Обычно вес брутто = вес нетто + вес упаковки

"weightN": Вес нетто в кг. Ищи:
- В графе 38 "Вес нетто (кг)" или просто "38"
- Слова "нетто", "вес нетто", "чистый вес", "Net weight"
- Числа рядом с "кг", "kg"
- Основной вес товара (обычно меньше веса брутто)

"count": Количество мест. Ищи:
- В графе "Количество мест" или числовых полях
- Слова "мест", "места", "общее кол. мест"
- "Общее кол. мест:" с числом после двоеточия
- Цифры рядом с этими словами

"declaration_type": "Тип декларации. Ищи:
- В графе "Тип декларации" 
- В начале всегда будет "ИМ" дальше 2 цифры (40, 73)
- Верни только цифры из типа декларации

"currency_total": Общая сумма. Ищи:
- В графе 12 или рядом с текстом "Общая сумма"
- Числа с плавающей точкой или с запятой
- Число рядом с валютой (например, "RUB", "USD", "EUR")
- Также может быть указано как "Сумма" или "итого"

"currency_rate": Курс валюты. Ищи:
- В графе 13 или рядом с текстом "курс"
- Может быть указано как "курс валюты", "курс", "Exchange rate", "Rate"
- Обычно число с плавающей точкой (например, 12591.57)

"declaration_number": Номер декларации. Ищи:
- номер декларации который начинается с "ED" И оставшееся значение

"recipient": Получатель/Импортер. Ищи:
- текст написанный полностью заглавными буквами
- ориентируйся на названия, похожие на следующий вид: "ИП ООО "BEKBORN" "

ВАЖНО:

Обязательно проверь название файла для vehicleId
Внимательно изучи ВСЕ поля и графы документа
Для веса брутто — ищи ЛЮБЫЕ числа больше веса нетто
Обрати особое внимание на графы 1, 21, 35, 38, 12, 13
Если какое-то поле не найдено — всё равно верни его со значением "none"
Отдай все данные в виде словаря (JSON) без лишнего текста.
                     """},
                    {
                        "type": "input_image",
                        "image_url": f"data:image/jpeg;base64,{image}",
                    },
                ],
            }
        ],
    )
    return response
